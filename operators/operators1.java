public class operators1{
    public static void main(String args[]){
        int A = 10;
        int B = 5;
        System.out.println(A==B);
        System.out.println(A!=B);
        System.out.println(A>B);
        System.out.println(A<B);
        System.out.println(A>=B);
        System.out.println(A<=B);


    }
}