public class operators{
    public static void main(String args[]){
        int A = 10;
        int B = 5;
        System.out.println(A+B);
        System.out.println(A/B);
        System.out.println(A%B);
        System.out.println("\n");

        int c = --A;
        System.out.println(A);
        System.out.println(c);

        int d = B++;
        System.out.println(B);
        System.out.println(d);
    }
}